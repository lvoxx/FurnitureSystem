    private void findAccountBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_findAccountBtnMouseClicked
        try {
            if (conn == null) {
                Settings.TryGetConnection(frame, conn);
            }
            GeneralQuery query = new GeneralQuery(conn);

            List<String> account = new ArrayList<>();
            account = query.getUserAuth(recoveryKeyTxt.getText());
            //System.out.println(account);
            if (account != null) {
                AlertDialog dialog = new AlertDialog(frame);
                dialog.showMessage("Your Account Info", "Username: " + account.get(0) + "\n Password: " + account.get(1));
                if (dialog.getMessageType().equals(MessageDialog.MessageType.OK)) {
                }
            } else {
                alert.setVisible(true);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_findAccountBtnMouseClicked

